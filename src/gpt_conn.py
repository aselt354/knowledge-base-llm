def get_link_type(event1: str, event2: str):
    user_prompt = (
        f"Оцени связь между двумя наблюдениями у одного пациента:\n\n"
        f"Наблюдение 1: «{event1}»\n"
        f"Наблюдение 2: «{event2}»\n\n"
        f"Определи тип связи."
    )
